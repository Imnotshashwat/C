#include <stdio.h>

int main()
{
    int a = 4;
    int b = 8;
    int c = a + b;
    printf("The value of a is %d and the value of b is %d and the sum is %d\n", a, b, c);
    // The modulus operator is used to get the remainder
    printf("The remainder when a is devide by b is %d\n", a%b);
    return 0;
}